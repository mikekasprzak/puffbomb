#include "SplashScreen.h"
// - ------------------------------------------------------------------------------------------ - //
#include <Graphics/Gfx.h>
// - ------------------------------------------------------------------------------------------ - //
#include <Global.h>
#include <Platform/Global.h>
// - ------------------------------------------------------------------------------------------ - //
extern int GetTime();
// - ------------------------------------------------------------------------------------------ - //
cSplashScreen::cSplashScreen()
{
	int EndTime = GetTime() + ( 3600 );
	
	while( GetTime() < EndTime )
	{
		MessageLoop();
		
		Gfx::ClearColorDepth();
			
		Gfx::LoadIdentity();
		Gfx::Translate( 0 , 0, -900 );

		Gfx::Circle( Vector3D( Real::Zero, Real::Zero, Real::Zero ), Real( 20 ), Gfx::White() );
		
		
		Gfx::SwapBuffers();
	}
	
}
