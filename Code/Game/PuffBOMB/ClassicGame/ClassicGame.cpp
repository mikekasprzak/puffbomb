// - ------------------------------------------------------------------------------------------ - //
cClassicGame::cClassicGame() {
	// Creating Engine //
	Log( LOG_HIGHEST_LEVEL, "Creating Engine..." );
	Engine = new cClassicGameEngine();
	
	Work();
}
// - ------------------------------------------------------------------------------------------ - //
cClassicGame::~cClassicGame() {
	delete Engine;
}
// - ------------------------------------------------------------------------------------------ - //
void cClassicGame::Draw() {
	Engine->Draw();
}
// - ------------------------------------------------------------------------------------------ - //
void cClassicGame::Step() {
	Engine->Step();
}
