// - ------------------------------------------------------------------------------------------ - //
#include "MenuFlow.h"
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
cMenuFlow::cMenuFlow( const std::string& FileName ) {
	// Creating Engine //
	Log( LOG_HIGHEST_LEVEL, "Creating Engine..." );
	Engine = new cMenuFlowEngine( FileName );
	
	Work();
}
// - ------------------------------------------------------------------------------------------ - //
cMenuFlow::~cMenuFlow() {
	delete Engine;
}
// - ------------------------------------------------------------------------------------------ - //
void cMenuFlow::Draw() {
	Engine->Draw();
}
// - ------------------------------------------------------------------------------------------ - //
void cMenuFlow::Step() {
	Engine->Step();
}
