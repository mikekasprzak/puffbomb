#ifndef __Geometry_ScalarVector3D_H__
#define __Geometry_ScalarVector3D_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#include <boost/operators.hpp>

#include "Vector3D.h"
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
// Utility class for reinterpreting a Vector as a scalar, so multiplication works differently //
// - ------------------------------------------------------------------------------------------ - //
class ScalarVector3D: public Vector3D,
	boost::multipliable2<ScalarVector3D, Vector3D>
{
public:
	inline ScalarVector3D() {
	}
	
	inline ScalarVector3D( const Real& _x, const Real& _y, const Real& _z ) :
		Vector3D( _x, _y, _z )
	{
	}
	
public:
	inline const Vector3D& operator *= ( const Vector3D& _Vs ) {
		x *= _Vs.x;
		y *= _Vs.y;
		z *= _Vs.z;
		return (const Vector3D&)*this;
	}
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_ScalarVector3D_H__ //
// - ------------------------------------------------------------------------------------------ - //
