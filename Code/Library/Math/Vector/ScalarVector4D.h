#ifndef __Geometry_ScalarVector4D_H__
#define __Geometry_ScalarVector4D_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#include <boost/operators.hpp>

#include "Vector4D.h"
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
// Utility class for reinterpreting a Vector as a scalar, so multiplication works differently //
// - ------------------------------------------------------------------------------------------ - //
class ScalarVector4D: public Vector4D,
	boost::multipliable2<ScalarVector4D, Vector4D>
{
public:
	inline ScalarVector4D() {
	}
	
	inline ScalarVector4D( const Real& _x, const Real& _y, const Real& _z, const Real& _w ) :
		Vector4D( _x, _y, _z, _w )
	{
	}
	
public:
	inline const Vector4D& operator *= ( const Vector4D& _Vs ) {
		x *= _Vs.x;
		y *= _Vs.y;
		z *= _Vs.z;
		w *= _Vs.w;
		return (const Vector4D&)*this;
	}
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_ScalarVector4D_H__ //
// - ------------------------------------------------------------------------------------------ - //
