#ifndef __Util_BasicFlags_H__
#define __Util_BasicFlags_H__
// - ------------------------------------------------------------------------------------------ - //
class cBasicFlags {
public:
	typedef unsigned int FlagType;	
	unsigned int Flags;

public:
	// - -------------------------------------------------------------------------------------- - //
	inline cBasicFlags() :
		Flags( 0 )
	{
	}
	// - -------------------------------------------------------------------------------------- - //
	inline cBasicFlags( FlagType _Flags ) :
		Flags( _Flags )
	{
	}
	// - -------------------------------------------------------------------------------------- - //	

public:
	// - -------------------------------------------------------------------------------------- - //
	// Flag testing function ------------------------------------------------------------------ - //
	inline const bool Is( const FlagType& _Flag ) const {
		return Flags & _Flag;
	}
	// - -------------------------------------------------------------------------------------- - //	

	// - -------------------------------------------------------------------------------------- - //
	inline cBasicFlags& Set( const FlagType& _Flags ) {
		Flags |= _Flags;
		return *this;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline cBasicFlags& Reset( const FlagType& _Flags ) {
		Flags &= ~_Flags;
		return *this;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline cBasicFlags& Set( const cBasicFlags& _Flags ) {
		Flags |= _Flags.Flags;
		return *this;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline cBasicFlags& Reset( const cBasicFlags& _Flags ) {
		Flags &= ~_Flags.Flags;
		return *this;
	}
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //
#endif // __Util_BasicFlags_H__ //
// - ------------------------------------------------------------------------------------------ - //
