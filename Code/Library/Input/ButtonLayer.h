#ifndef __Input_ButtonLayer_H__
#define __Input_ButtonLayer_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#include <sys/types.h>

#include "Button.h"
#include "KeyboardDevice.h"
// - ------------------------------------------------------------------------------------------ - //

namespace Input {

// - ------------------------------------------------------------------------------------------ - //
	class cButtonLayer {
	public:
		inline cButton& operator [] ( size_t Index ) {
			if ( Index < KEY_OFFSET ) {
				return Keyboard.Button[ Index ];
			}
			else {
				int PadNumber = (Index - KEY_OFFSET) >> 6;
				if ( PadNumber == 0 )				
					return Mouse.Button[ Index - KEY_OFFSET ];
				else
					return Pad[ PadNumber ].Button[ Index & 31 ];
			}
		}
	};
// - ------------------------------------------------------------------------------------------ - //

};

// - ------------------------------------------------------------------------------------------ - //
#endif // __Input_ButtonLayer_H__ //
// - ------------------------------------------------------------------------------------------ - //
