// - ------------------------------------------------------------------------------------------ - //
// Rect2D //
// - ------------------------------------------------------------------------------------------ - //
#ifndef __Geometry_Rect2D_H__
#define __Geometry_Rect2D_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#include <Geometry/Real.h>
#include <Geometry/Vector.h>
// - ------------------------------------------------------------------------------------------ - //

class Rect2D {
public:
	// - -------------------------------------------------------------------------------------- - //
	Vector2D _P1;
	Vector2D _Shape;
	// - -------------------------------------------------------------------------------------- - //
public:
	// - -------------------------------------------------------------------------------------- - //
	inline Rect2D() {
	}
	// - -------------------------------------------------------------------------------------- - //
	
	// - -------------------------------------------------------------------------------------- - //
	// Normal Creation (RectXD style (x,y,w,h)) //
	// - -------------------------------------------------------------------------------------- - //
	inline Rect2D( const Real& _x, const Real& _y, const Real& _w, const Real& _h ) :
		_P1( _x, _y ),
		_Shape( _w, _h )
	{
	}
	// - -------------------------------------------------------------------------------------- - //
	inline Rect2D( const Vector2D& _Vec1, const Vector2D& _Vec2 ) :
		_P1( _Vec1 ),
		_Shape( _Vec2 )
	{
	}
	// - -------------------------------------------------------------------------------------- - //

	// - -------------------------------------------------------------------------------------- - //
	// Pair Creation (P1, P2) //
	// - -------------------------------------------------------------------------------------- - //
	inline Rect2D( const bool&, const Vector2D& _Vec1, const Vector2D& _Vec2 ) :
		_P1( _Vec1.x.Min( _Vec2.x ), _Vec1.y.Min( _Vec2.y ) ),
		_Shape( (_Vec2.x - _Vec1.x).Abs(), (_Vec2.y - _Vec1.y).Abs() )
	{
	}
	// - -------------------------------------------------------------------------------------- - //
	inline Rect2D( const bool&, const bool&, const Vector2D& _Vec1, const Vector2D& _Vec2 ) :
		_P1( _Vec1 ),
		_Shape( _Vec2 - _Vec1 )
	{
	}
	// - -------------------------------------------------------------------------------------- - //

	// - -------------------------------------------------------------------------------------- - //
	// Radius Creation (Center, HalfShape) //
	// - -------------------------------------------------------------------------------------- - //
	inline Rect2D( const bool&, const bool&, const bool&, const Vector2D& _VecCenter, const Vector2D& _VecShape ) :
		_P1( _VecCenter - _VecShape ),
		_Shape( _VecShape + _VecShape )
	{
	}
	// - -------------------------------------------------------------------------------------- - //

public:
	// - -------------------------------------------------------------------------------------- - //
	// Creation as a Pair //
	// - -------------------------------------------------------------------------------------- - //
	inline static const Rect2D Pair( const Real& _x1, const Real& _y1, const Real& _x2, const Real& _y2 ) {
		return Rect2D( true, Vector2D( _x1, _y1 ), Vector2D( _x2, _y2 ) );
	}
	// - -------------------------------------------------------------------------------------- - //
	inline static const Rect2D Pair( const Vector2D& _Vec1, const Vector2D& _Vec2 ) {
		return Rect2D( true, _Vec1, _Vec2 );
	}
	// - -------------------------------------------------------------------------------------- - //
	// Optimized Pair Creation (both points must be in the correct space, no min/max'ing done) //
	// - -------------------------------------------------------------------------------------- - //
	inline static const Rect2D _Pair( const Vector2D& _Vec1, const Vector2D& _Vec2 ) {
		return Rect2D( true, true, _Vec1, _Vec2 );
	}
	// - -------------------------------------------------------------------------------------- - //	

	// - -------------------------------------------------------------------------------------- - //
	// Creation as a Radius //
	// - -------------------------------------------------------------------------------------- - //
	inline static const Rect2D Radius( const Real& _x1, const Real& _y1, const Real& _x2, const Real& _y2 ) {
		return Rect2D( true, true, true, Vector2D( _x1, _y1 ), Vector2D( _x2, _y2 ) );
	}
	// - -------------------------------------------------------------------------------------- - //
	inline static const Rect2D Radius( const Vector2D& _Vec1, const Vector2D& _Vec2 ) {
		return Rect2D( true, true, true, _Vec1, _Vec2 );
	}
	// - -------------------------------------------------------------------------------------- - //

public:
	// - -------------------------------------------------------------------------------------- - //
	inline const Vector2D P1() const {
		return _P1;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const Vector2D P2() const {
		return _P1 + _Shape;
	}
	// - -------------------------------------------------------------------------------------- - //

	// - -------------------------------------------------------------------------------------- - //
	inline const Vector2D Center() const {
		return P1() + HalfShape();
	}
	// - -------------------------------------------------------------------------------------- - //

	// - -------------------------------------------------------------------------------------- - //
	inline const Real& Width() const {
		return _Shape.x;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const Real HalfWidth() const {
		return Width() * Real::Half;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const Real& Height() const {
		return _Shape.y;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const Real HalfHeight() const {
		return Height() * Real::Half;
	}
	// - -------------------------------------------------------------------------------------- - //	

	// - -------------------------------------------------------------------------------------- - //
	inline const Vector2D Shape() const {
		return _Shape;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const Vector2D HalfShape() const {
		return Shape() * Real::Half;
	}
	// - -------------------------------------------------------------------------------------- - //
public:	
	// - -------------------------------------------------------------------------------------- - //
	inline const Real Area() const {
		return Width() * Height();
	}
	// - -------------------------------------------------------------------------------------- - //

public:	
	// - -------------------------------------------------------------------------------------- - //
	// Test for intersection between Rectangles //
	inline const bool operator == ( const Rect2D& Vs ) const;
	// - -------------------------------------------------------------------------------------- - //
	inline const bool operator != ( const Rect2D& Vs ) const {
		return !(*this == Vs);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const bool operator == ( const Vector2D& Vs ) const;
	// - -------------------------------------------------------------------------------------- - //
	// Rectangle Difference //
	inline const Rect2D operator - ( const Rect2D& Vs ) const {
		return Pair(
			Vector2D( P1().x.Max( Vs.P1().x ), P1().y.Max( Vs.P1().y ) ),
			Vector2D( P2().x.Min( Vs.P2().x ), P2().y.Min( Vs.P2().y ) )
			);
	}
	// - -------------------------------------------------------------------------------------- - //
	// Rectangle Union //
	inline const Rect2D operator + ( const Rect2D& Vs ) const {
		return Pair(
			Vector2D( P1().x.Min( Vs.P1().x ), P1().y.Min( Vs.P1().y ) ),
			Vector2D( P2().x.Max( Vs.P2().x ), P2().y.Max( Vs.P2().y ) )
			);
	}
	// - -------------------------------------------------------------------------------------- - //
	// Given a point, find the closest point on the Rectangle //
	inline Vector2D ClosestPoint( const Vector2D& v ) const {
		Vector2D Point;
	
		if ( v.x < P1().x )
			Point.x = P1().x;
		else if ( v.x > P2().x )
			Point.x = P2().x;
		else
			Point.x = v.x;
	
		if ( v.y < P1().y )
			Point.y = P1().y;
		else if ( v.y > P2().y )
			Point.y = P2().y;
		else
			Point.y = v.y;
	
		return Point;
	}
	// - -------------------------------------------------------------------------------------- - //
public:
	// - -------------------------------------------------------------------------------------- - //
	// Adding vectors to offset the position of a rectangle //
	inline const Rect2D operator + ( const Vector2D& Vs ) const {
		return Pair( P1() + Vs, P2() + Vs );
	}
	// - -------------------------------------------------------------------------------------- - //
	// Subtracting vectors to offset the position of a rectangle //
	inline const Rect2D operator - ( const Vector2D& Vs ) const {
		return Pair( P1() - Vs, P2() - Vs );
	}
	// - -------------------------------------------------------------------------------------- - //
public:
	inline const class RadiusRect2D ToRadiusRect() const;
	inline const class PairRect2D ToPairRect() const;
	inline const Rect2D& ToRect() const {
		return *this;
	}
};

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_Rect2D_H__ //
// - ------------------------------------------------------------------------------------------ - //
