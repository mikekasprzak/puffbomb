#ifndef __Geometry_BGRASet_H__
#define __Geometry_BGRASet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class BGRASet {
public:
	T b, g, r, a;

public:
	BGRASet() {
	}

	BGRASet( T _b, T _g, T _r, T _a ) :
		b( _b ),
		g( _g ),
		r( _r ),
		a( _a )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const BGRASet& _Vs ) const {
		if ( (b - _Vs.b) == 0 )
			if ( (g - _Vs.g) == 0 )
				if ( (r - _Vs.r) == 0 )
					return (a - _Vs.a) == 0;
		return false;
	}

};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_BGRASet_H__ //
// - ------------------------------------------------------------------------------------------ - //
