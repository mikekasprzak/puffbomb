cWindowManager::cWindowManager()
{
	
}
// - ------------------------------------------------------------------------------------------ - //
cWindowManager::~cWindowManager()
{
	Log( 10, "Deleting Window Manager" );

	for( size_t idx = 0; idx < Form.size(); ++idx )
	{
		delete Form[ idx ];
	}
}
// - ------------------------------------------------------------------------------------------ - //
void cWindowManager::Load( const std::string& FileName )
{
	Form.push_back( new cForm( FileName ) );
}
