#ifndef __Geometry_ABCDSet_H__
#define __Geometry_ABCDSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class ABCDSet {
public:
	T a, b, c, d;

public:
	ABCDSet() {
	}

	ABCDSet( T _a, T _b, T _c, T _d ) :
		a( _a ),
		b( _b ),
		c( _c ),
		d( _d )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const ABCDSet& _Vs ) const {
		if ( (a - _Vs.a) == 0 )
			if ( (b - _Vs.b) == 0 )
				if ( (c - _Vs.c) == 0 )
					return (d - _Vs.d) == 0;
		return false;
	}

	
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_ABCDSet_H__ //
// - ------------------------------------------------------------------------------------------ - //
