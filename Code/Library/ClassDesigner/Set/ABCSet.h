#ifndef __Geometry_ABCSet_H__
#define __Geometry_ABCSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class ABCSet {
public:
	T a, b, c;

public:
	ABCSet() {
	}
	
	ABCSet( T _a, T _b, T _c ) :
		a( _a ),
		b( _b ),
		c( _c )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const ABCSet& _Vs ) const {
		if ( (a - _Vs.a) == 0 )
			if ( (b - _Vs.b) == 0 )
			return (c - _Vs.c) == 0;
		return false;
	}

};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_ABCSet_H__ //
