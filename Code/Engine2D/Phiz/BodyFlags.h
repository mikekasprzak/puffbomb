union cBodyFlags {
// - ------------------------------------------------------------------------------------------ - //
	struct {
		unsigned int Collision:24;
		unsigned int State:8;
	};
// - ------------------------------------------------------------------------------------------ - //
	unsigned int All;
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
	// State Flags ---------------- //
	enum {
		flActive = bit7,
	};
	// - -------------------------------------------------------------------------------------- - //
	inline void Init() {
		All = flActive << 24;
//		State = flActive;
//		Collision = 0;
	}
	// - -------------------------------------------------------------------------------------- - //	
	inline void Clear() {
		Collision = 0;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const bool IsActive() const {
		return State & flActive;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline void SetActive() {
		State |= flActive;
	}	
	// - -------------------------------------------------------------------------------------- - //
	inline void SetInactive() {
		State &= ~flActive;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline void Set( const cCollisionFlags& _Collision ) {
		Collision |= _Collision.Flags;
	}
	// - -------------------------------------------------------------------------------------- - //
// - ------------------------------------------------------------------------------------------ - //
};
// - ------------------------------------------------------------------------------------------ - //
//}; // namespace Engine2D //
// - ------------------------------------------------------------------------------------------ - //
#endif // __Engine2D_Physics_BodyFlags_H__ //
