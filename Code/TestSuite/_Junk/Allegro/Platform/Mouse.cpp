namespace Platform {
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
cMouse Mouse;
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
cMouse::cMouse() :
	_Pos( Real( mouse_x ) / Real( ScreenW ), Real( mouse_y ) / Real( ScreenH ) ),
	_Old( _Pos )
{
	
}
// - ------------------------------------------------------------------------------------------ - //
void cMouse::Update() {
	_Old = _Pos;
	
	_Pos.x = mouse_x;
	_Pos.x /= ScreenW;
	_Pos.y = mouse_y;
	_Pos.y /= ScreenH;
}	
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
};
