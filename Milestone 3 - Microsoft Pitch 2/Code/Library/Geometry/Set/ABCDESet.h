#ifndef __Geometry_ABCDESet_H__
#define __Geometry_ABCDESet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class ABCDESet {
public:
	T a, b, c, d, e;

public:
	ABCDESet() {
	}

	ABCDESet( T _a, T _b, T _c, T _d, T _e ) :
		a( _a ),
		b( _b ),
		c( _c ),
		d( _d ),
		e( _e )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const ABCDESet& _Vs ) const {
		if ( (a - _Vs.a) == 0 )
			if ( (b - _Vs.b) == 0 )
				if ( (c - _Vs.c) == 0 )
					if ( (d - _Vs.d) == 0 )
						return (e - _Vs.e) == 0;
		return false;
	}

};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_ABCDESet_H__ //
// - ------------------------------------------------------------------------------------------ - //
