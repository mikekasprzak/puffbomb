#ifndef __Input_PadDevice_H__
#define __Input_PadDevice_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#include <sys/types.h>

#include "Button.h"
#include "Axis.h"
#include "Axis2D.h"
// - ------------------------------------------------------------------------------------------ - //

namespace Input {

// - ------------------------------------------------------------------------------------------ - //
	class cPadDevice {
	public:
		// - ---------------------------------------------------------------------------------- - //
		// A GamePad has 10 buttons //
		// - ---------------------------------------------------------------------------------- - //
		// 4 Face Buttons //
		// 2 Trigger Buttons //
		// 2 Stick Buttons //
		// 2 Utility (Start/Select) Buttons //
		// - ---------------------------------------------------------------------------------- - //
		cButton Button[PAD_BUTTON_COUNT];
		// - ---------------------------------------------------------------------------------- - //		
		inline size_t ButtonCount() {
			return sizeof( Button ) / sizeof( cButton );
		}
		// - ---------------------------------------------------------------------------------- - //

		// - ---------------------------------------------------------------------------------- - //
		// A GamePad has 7 axises //
		// - ---------------------------------------------------------------------------------- - //
		// 2 Per Stick, and 2 sticks //
		// 2 for the D-Pad //
		// 1 Unified Axis for the L/R triggers (to utilize a design flaw of the 360 controller) //
		// - ---------------------------------------------------------------------------------- - //
		cAxis2D Stick1;
		cAxis2D Stick2;
		cAxis2D DPad;
		
		cAxis Trigger;
		// - ---------------------------------------------------------------------------------- - //
	};
// - ------------------------------------------------------------------------------------------ - //

};

// - ------------------------------------------------------------------------------------------ - //
#endif // __Input_PadDevice_H__ //
// - ------------------------------------------------------------------------------------------ - //
