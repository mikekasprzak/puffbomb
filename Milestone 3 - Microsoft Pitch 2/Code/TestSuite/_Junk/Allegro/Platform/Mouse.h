class cMouse {
	Vector2D _Pos;
	Vector2D _Old;
public:
	Real Wheel;
	int Button;


public:
	inline const Vector2D& Pos() {
		return _Pos;
	}

	inline const Vector2D& Old() {
		return _Old;
	}
	
	inline Vector2D Diff() {
		return _Old - _Pos;
	}

public:
	cMouse();
	
	void Update();
};
