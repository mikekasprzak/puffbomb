// - ------------------------------------------------------------------------------------------ - //
cGolfGame::cGolfGame() {
	// Creating Engine //
	Log( LOG_HIGHEST_LEVEL, "Creating Engine..." );
	Engine = new cGolfGameEngine();
	
	Work();
}
// - ------------------------------------------------------------------------------------------ - //
cGolfGame::~cGolfGame() {
	delete Engine;
}
// - ------------------------------------------------------------------------------------------ - //
void cGolfGame::Draw() {
	Engine->Draw();
}
// - ------------------------------------------------------------------------------------------ - //
void cGolfGame::Step() {
	Engine->Step();
}
