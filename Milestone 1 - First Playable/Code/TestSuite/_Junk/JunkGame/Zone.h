class cZone {
public:
	typedef RadiusRect2D RectType;
	RectType BoundingRect;
	
	int Id;
	int Argument;
	
public:
	cZone( const Real x1, const Real y1, const Real x2, const Real y2 ) :
		BoundingRect( RectType::Pair( x1, y1, x2, y2 ) ),
		Id( 0 ),
		Argument( 0 )
	{
	}
	cZone( const Vector2D Pos1, const Vector2D Pos2 ) :
		BoundingRect( RectType::Pair( Pos1, Pos2 ) ),
		Id( 0 ),
		Argument( 0 )
	{
	}
	
	void Action( const class cSphereObject& Vs );
	//void Action( const class cPolyObject& Vs );
	
	bool InitZone( );
};
