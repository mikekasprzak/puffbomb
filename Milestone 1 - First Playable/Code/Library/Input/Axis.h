#ifndef __Input_Axis_H__
#define __Input_Axis_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#include <Geometry/Real.h>
// - ------------------------------------------------------------------------------------------ - //

namespace Input {

// - ------------------------------------------------------------------------------------------ - //
	class cAxis {
	private:
		Real _Current;
		Real _Last;
	
	public:
		// - ---------------------------------------------------------------------------------- - //
		inline const Real Current() const {
			return _Current;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const Real Last() const {
			return _Last;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const Real Diff() const {
			return (_Last - _Current);
		}
		// - ---------------------------------------------------------------------------------- - //

		// - ---------------------------------------------------------------------------------- - //
		inline operator Real () {
			return Current();
		}
		// - ---------------------------------------------------------------------------------- - //
	
	public:
		// - ---------------------------------------------------------------------------------- - //
		inline cAxis() {
		}	
		// - ---------------------------------------------------------------------------------- - //
		inline void Reset() {
			_Current = Real::Zero;
			_Last = Real::Zero;
		}
		// - ---------------------------------------------------------------------------------- - //		
		inline void Update( Real NewState ) {
			_Last = _Current;
			_Current = NewState;
		}
		// - ---------------------------------------------------------------------------------- - //
	};
// - ------------------------------------------------------------------------------------------ - //

};

// - ------------------------------------------------------------------------------------------ - //
#endif // __Input_Axis_H__ //
// - ------------------------------------------------------------------------------------------ - //
