#ifndef __Engine2D_Physics_Body2D_Constraints_SphereFlags_H__
#define __Engine2D_Physics_Body2D_Constraints_SphereFlags_H__
// - ------------------------------------------------------------------------------------------ - //
#include <Util/ClassDesigner.h>
// - ------------------------------------------------------------------------------------------ - //
namespace Engine2D {
// - ------------------------------------------------------------------------------------------ - //
class cSphereFlags: public cBasicFlags {
public:
	// - -------------------------------------------------------------------------------------- - //
	// Sphere Flags ---------------- //
	enum {
		flSensor = bit0,		// A sphere that tests, but doesn't solve collisions //
		flActuator = bit1,		// A sphere that sends a message on contact (i.e. damage) //
	};
	// - -------------------------------------------------------------------------------------- - //

public:
	// - -------------------------------------------------------------------------------------- - //
	inline cSphereFlags() :
		cBasicFlags( 0 )
	{
	}
	// - -------------------------------------------------------------------------------------- - //
	inline cSphereFlags( const FlagType _Flags ) :
		cBasicFlags( _Flags )
	{
	}
	// - -------------------------------------------------------------------------------------- - //

public:
	// - -------------------------------------------------------------------------------------- - //
	// Create functions for setting individual flags ------------------------------------------ - //
	// - -------------------------------------------------------------------------------------- - //
	#define __FLAG_FUNCTION( _flag ) \
	inline cSphereFlags& Set ## _flag() { \
		Flags |= fl ## _flag; \
		return *this; \
	}
	// - -------------------------------------------------------------------------------------- - //
	__FLAG_FUNCTION( Sensor );
	__FLAG_FUNCTION( Actuator );
	// - -------------------------------------------------------------------------------------- - //
	#undef __FLAG_FUNCTION
	// - -------------------------------------------------------------------------------------- - //

public:
	// - -------------------------------------------------------------------------------------- - //
	// Create functions for resetting individual flags ---------------------------------------- - //
	// - -------------------------------------------------------------------------------------- - //
	#define __FLAG_FUNCTION( _flag ) \
	inline cSphereFlags& Reset ## _flag() { \
		Flags &= ~(fl ## _flag); \
		return *this; \
	}
	// - -------------------------------------------------------------------------------------- - //
	__FLAG_FUNCTION( Sensor );
	__FLAG_FUNCTION( Actuator );
	// - -------------------------------------------------------------------------------------- - //
	#undef __FLAG_FUNCTION
	// - -------------------------------------------------------------------------------------- - //

public:
	// - -------------------------------------------------------------------------------------- - //
	// Create functions for Testing individual flags ------------------------------------------ - //
	// - -------------------------------------------------------------------------------------- - //
	#define __FLAG_FUNCTION( _flag ) \
	inline const bool _flag() const { \
		return Flags & (fl ## _flag); \
	}
	// - -------------------------------------------------------------------------------------- - //
	__FLAG_FUNCTION( Sensor );
	__FLAG_FUNCTION( Actuator );
	// - -------------------------------------------------------------------------------------- - //
	#undef __FLAG_FUNCTION
	// - -------------------------------------------------------------------------------------- - //

public:
	// - -------------------------------------------------------------------------------------- - //
	// Create functions for Testing individual flags only ------------------------------------- - //
	// - -------------------------------------------------------------------------------------- - //
	#define __FLAG_FUNCTION( _flag ) \
	inline const bool Only ## _flag() const { \
		return Flags == (fl ## _flag); \
	}
	// - -------------------------------------------------------------------------------------- - //
	__FLAG_FUNCTION( Sensor );
	__FLAG_FUNCTION( Actuator );
	// - -------------------------------------------------------------------------------------- - //
	#undef __FLAG_FUNCTION
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //
}; // namespace Engine2D //
// - ------------------------------------------------------------------------------------------ - //
#endif // __Engine2D_Physics_Body2D_Constraints_SphereFlags_H__ //
// - ------------------------------------------------------------------------------------------ - //
