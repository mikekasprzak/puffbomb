#ifndef __Geometry_ABSet_H__
#define __Geometry_ABSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class ABSet {
public:
	T a, b;

public:
	ABSet() {
	}

	ABSet( T _a, T _b ) :
		a( _a ),
		b( _b )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const ABSet& _Vs ) const {
		if ( (a - _Vs.a) == 0 )
			return (b - _Vs.b) == 0;
		return false;
	}

};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_ABSet_H__ //
