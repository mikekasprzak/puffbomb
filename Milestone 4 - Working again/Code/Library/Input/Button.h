#ifndef __Input_Button_H__
#define __Input_Button_H__
// - ------------------------------------------------------------------------------------------ - //

namespace Input {

// - ------------------------------------------------------------------------------------------ - //
	class cButton {
	private:
		bool _Current;
		bool _Last;
	
	public:
		// - ---------------------------------------------------------------------------------- - //
		inline const bool Current() const {
			return _Current;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const bool Last() const {
			return _Last;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const bool Pressed() const {
			return (_Current != _Last) && (_Last == false);
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const bool Released() const {
			return (_Current != _Last) && (_Last == true);
		}
		// - ---------------------------------------------------------------------------------- - //

		// - ---------------------------------------------------------------------------------- - //
		inline operator bool () {
			return Current();
		}
		// - ---------------------------------------------------------------------------------- - //

	public:
		// - ---------------------------------------------------------------------------------- - //
		inline cButton() :
			_Current( false ),
			_Last( false )
		{
		}
		// - ---------------------------------------------------------------------------------- - //
		inline void Reset() {
			_Current = false;
			_Last = false;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline void Update( bool NewState ) {
			_Last = _Current;
			_Current = NewState;
		}
		// - ---------------------------------------------------------------------------------- - //
	};
// - ------------------------------------------------------------------------------------------ - //

};

// - ------------------------------------------------------------------------------------------ - //
#endif // __Input_Button_H__ //
// - ------------------------------------------------------------------------------------------ - //
