// - ------------------------------------------------------------------------------------------ - //
#include "MenuFlow.h"
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
cMenuFlow::cMenuFlow( const std::string& FileName, cClassicSaveData* _ClassicSaveData ) {
	// Creating Engine //
	Log( LOG_HIGHEST_LEVEL, "Creating Engine..." );
	Engine = new cMenuFlowEngine( FileName, _ClassicSaveData );
	
	Work();
}
// - ------------------------------------------------------------------------------------------ - //
cMenuFlow::~cMenuFlow() {
	delete Engine;
}
// - ------------------------------------------------------------------------------------------ - //
void cMenuFlow::Draw() {
	Engine->Draw();
}
// - ------------------------------------------------------------------------------------------ - //
void cMenuFlow::Step() {
	Engine->Step();
	
	if( Engine->MenuManager.BreakLoop )
	{
		BreakLoop = true;
	}
}
