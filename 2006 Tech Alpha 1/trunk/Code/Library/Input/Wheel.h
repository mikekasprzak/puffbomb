#ifndef __Input_Wheel_H__
#define __Input_Wheel_H__
// - ------------------------------------------------------------------------------------------ - //

namespace Input {

// - ------------------------------------------------------------------------------------------ - //
	class cWheel {
	private:
		int _Temp;
		
		int _Current;
		int _Last;
	
	public:
		// - ---------------------------------------------------------------------------------- - //
		inline const int Current() const {
			return _Current;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const int Last() const {
			return _Last;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const int Diff() const {
			return (_Last - _Current);
		}
		// - ---------------------------------------------------------------------------------- - //

		// - ---------------------------------------------------------------------------------- - //
		inline operator int () {
			return _Current;//Diff();
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const int operator ++ (int) {
			return _Temp++;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const int& operator ++ () {
			return ++_Temp;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const int operator -- (int) {
			return _Temp--;
		}
		// - ---------------------------------------------------------------------------------- - //
		inline const int& operator -- () {
			return --_Temp;
		}
		// - ---------------------------------------------------------------------------------- - //
	
	public:
		// - ---------------------------------------------------------------------------------- - //
		inline cWheel() :
			_Temp( 0 ),
			_Current( 0 ),
			_Last( 0 )
		{
		}	
		// - ---------------------------------------------------------------------------------- - //
		inline void Reset() {
			_Current = 0;
			_Last = 0;
		}
		// - ---------------------------------------------------------------------------------- - //		
		inline void Update( ) {
			_Last = _Current;
			_Current = _Temp;
		}
		// - ---------------------------------------------------------------------------------- - //
	};
// - ------------------------------------------------------------------------------------------ - //

};

// - ------------------------------------------------------------------------------------------ - //
#endif // __Input_Wheel_H__ //
// - ------------------------------------------------------------------------------------------ - //
