#ifndef __Util_StaticArray_H__
#define __Util_StaticArray_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template< class Type >
class StaticArray {
	size_t _Size;
	Type Data[0];
public:
	inline const size_t& Size() const {
		return _Size;
	}
	
	inline Type& operator [] ( size_t Index ) {
		return Data[ Index ];
	}

	inline Type& operator * () {
		return Data[ 0 ];
	}
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Util_StaticArray_H__ //
